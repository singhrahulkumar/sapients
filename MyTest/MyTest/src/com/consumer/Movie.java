package com.consumer;

public class Movie {

	
	String name;
	String hero;
	String heroi;
	public Movie(String name, String hero, String heroi) {
		super();
		this.name = name;
		this.hero = hero;
		this.heroi = heroi;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getHero() {
		return hero;
	}
	public void setHero(String hero) {
		this.hero = hero;
	}
	public String getHeroi() {
		return heroi;
	}
	public void setHeroi(String heroi) {
		this.heroi = heroi;
	};
	
	
}
